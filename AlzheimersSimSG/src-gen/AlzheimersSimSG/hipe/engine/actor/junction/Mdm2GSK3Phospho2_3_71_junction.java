public class Mdm2GSK3Phospho2_3_71_junction extends GenericJunctionActor{
	
	@Override
	protected void initializePorts(Map<String, ActorRef> name2actor, JunctionNode node) {
		ports = new LinkedList<>();
		ports.add(new PortJunction(getSelf(), name2actor.get("Mdm2GSK3Phospho2_3_production"), this::check_constraint_1));
	}
	
	@Override
	protected void registerMatchForAttributeChanges(HMatch match) {
	}
	
	@Override
	protected void deregisterMatchForAttributeChanges(Set<HMatch> matches, HMatch match) {
	}
	
	@Override
	protected void changeAttribute(AttributeChanged<HMatch> message) {
		for(Port<?> port : ports) {
			message.initialMessage.increment();
			port.forwardMessage(message);
		}
		
		message.initialMessage.decrement();
	}
	
	public boolean check_constraint_1(HMatch match) {
		AlzheimerModel.P53 p53_1 = (AlzheimerModel.P53) match.getNodes()[5];
		AlzheimerModel.P53 p53_2 = (AlzheimerModel.P53) match.getNodes()[1];
		boolean predicate = !p53_2.equals(p53_1);
		match.setConstraintSatisfied(predicate);
		return predicate;
	}
	
}

